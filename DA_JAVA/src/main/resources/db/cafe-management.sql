/* Cancelled orders are used by the admin order-management screen. */
IF OBJECT_ID(N'dbo.Orders', N'U') IS NOT NULL
   AND NOT EXISTS (
       SELECT 1
       FROM sys.check_constraints
       WHERE parent_object_id = OBJECT_ID(N'dbo.Orders')
         AND definition LIKE '%status%'
         AND definition LIKE '%cancelled%'
   )
BEGIN
    DECLARE @statusConstraint SYSNAME;
    SELECT TOP (1) @statusConstraint = name
    FROM sys.check_constraints
    WHERE parent_object_id = OBJECT_ID(N'dbo.Orders')
      AND definition LIKE '%status%';

    IF @statusConstraint IS NOT NULL
    BEGIN
        EXEC (N'ALTER TABLE dbo.Orders DROP CONSTRAINT [' + @statusConstraint + N']');
    END;

    ALTER TABLE dbo.Orders WITH CHECK
        ADD CONSTRAINT CK_Orders_status
        CHECK (status IN ('pending', 'paid', 'cancelled'));
END;
GO

