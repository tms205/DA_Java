CREATE TRIGGER trg_UpdateOrderTotal
ON OrderDetail
AFTER INSERT, UPDATE, DELETE
AS
BEGIN
    SET NOCOUNT ON;

    UPDATE o
    SET total = (
        SELECT ISNULL(SUM(od.quantity * od.price), 0)
        FROM OrderDetail od
        WHERE od.order_id = o.order_id
    )
    FROM Orders o
    WHERE o.order_id IN (
        SELECT order_id FROM inserted
        UNION
        SELECT order_id FROM deleted
    );
END;
GO

CREATE TRIGGER trg_PreventAddToPaidOrder
ON OrderDetail
AFTER INSERT, UPDATE
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS (
        SELECT 1
        FROM inserted i
        JOIN Orders o ON i.order_id = o.order_id
        WHERE o.status = 'paid'
    )
    BEGIN
        RAISERROR(N'Không thể thêm hoặc sửa món trong đơn hàng đã thanh toán', 16, 1);
        ROLLBACK TRANSACTION;
    END
END;
GO

CREATE TRIGGER trg_UpdateTableStatusOnInsert
ON Orders
AFTER INSERT
AS
BEGIN
    SET NOCOUNT ON;

    UPDATE t
    SET status = 'occupied'
    FROM TableCafe t
    JOIN inserted i ON t.table_id = i.table_id
    WHERE i.status = 'pending'
      AND i.table_id IS NOT NULL;
END;
GO

CREATE TRIGGER trg_UpdateTableStatusOnUpdate
ON Orders
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;

    IF UPDATE(status)
    BEGIN
        UPDATE t
        SET status = CASE WHEN i.status = 'paid' THEN 'empty' ELSE 'occupied' END
        FROM TableCafe t
        JOIN inserted i ON t.table_id = i.table_id
        JOIN deleted d ON i.order_id = d.order_id
        WHERE i.status <> d.status
          AND i.table_id IS NOT NULL;
    END
END;
GO

CREATE PROCEDURE sp_CreateOrder
    @table_id INT,
    @user_id INT
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS (SELECT 1 FROM TableCafe WHERE table_id = @table_id AND status = 'occupied')
    BEGIN
        RAISERROR(N'Bàn đang có người, không thể tạo đơn mới', 16, 1);
        RETURN;
    END;

    IF NOT EXISTS (SELECT 1 FROM [User] WHERE user_id = @user_id AND is_active = 1)
    BEGIN
        RAISERROR(N'Nhân viên không tồn tại hoặc đã bị vô hiệu', 16, 1);
        RETURN;
    END;

    INSERT INTO Orders(table_id, user_id)
    VALUES(@table_id, @user_id);

    SELECT CONVERT(INT, SCOPE_IDENTITY()) AS order_id;
END;
GO

CREATE PROCEDURE sp_AddProductToOrder
    @order_id INT,
    @product_id INT,
    @quantity INT
AS
BEGIN
    SET NOCOUNT ON;

    IF @quantity <= 0
    BEGIN
        RAISERROR(N'Số lượng phải lớn hơn 0', 16, 1);
        RETURN;
    END;

    IF NOT EXISTS (SELECT 1 FROM Orders WHERE order_id = @order_id)
    BEGIN
        RAISERROR(N'Đơn hàng không tồn tại', 16, 1);
        RETURN;
    END;

    IF EXISTS (SELECT 1 FROM Orders WHERE order_id = @order_id AND status = 'paid')
    BEGIN
        RAISERROR(N'Đơn hàng đã thanh toán, không thể thêm món', 16, 1);
        RETURN;
    END;

    IF NOT EXISTS (SELECT 1 FROM Product WHERE product_id = @product_id AND is_available = 1)
    BEGIN
        RAISERROR(N'Sản phẩm không tồn tại hoặc đã ngừng bán', 16, 1);
        RETURN;
    END;

    DECLARE @price DECIMAL(10,2);
    SELECT @price = price FROM Product WHERE product_id = @product_id;

    IF EXISTS (SELECT 1 FROM OrderDetail WHERE order_id = @order_id AND product_id = @product_id)
    BEGIN
        UPDATE OrderDetail
        SET quantity = quantity + @quantity
        WHERE order_id = @order_id AND product_id = @product_id;
    END
    ELSE
    BEGIN
        INSERT INTO OrderDetail(order_id, product_id, quantity, price)
        VALUES(@order_id, @product_id, @quantity, @price);
    END;
END;
GO

CREATE PROCEDURE sp_PayOrder
    @order_id INT,
    @method VARCHAR(50)
AS
BEGIN
    SET NOCOUNT ON;

    IF @method NOT IN ('cash','momo','bank')
    BEGIN
        RAISERROR(N'Phương thức thanh toán không hợp lệ', 16, 1);
        RETURN;
    END;

    IF NOT EXISTS (SELECT 1 FROM Orders WHERE order_id = @order_id AND status = 'pending')
    BEGIN
        RAISERROR(N'Đơn hàng không tồn tại hoặc đã được thanh toán', 16, 1);
        RETURN;
    END;

    DECLARE @total DECIMAL(12,2);
    SELECT @total = total FROM Orders WHERE order_id = @order_id;

    IF @total <= 0
    BEGIN
        RAISERROR(N'Đơn hàng chưa có món', 16, 1);
        RETURN;
    END;

    UPDATE Orders
    SET status = 'paid'
    WHERE order_id = @order_id;

    INSERT INTO Payment(order_id, method, amount_paid)
    VALUES(@order_id, @method, @total);
END;
GO

CREATE PROCEDURE sp_GetDailyRevenueReport
    @date DATE
AS
BEGIN
    SET NOCOUNT ON;

    SELECT
        @date AS report_date,
        COUNT(DISTINCT o.order_id) AS total_orders,
        ISNULL(SUM(o.total), 0) AS total_revenue,
        COUNT(DISTINCT CASE WHEN p.method = 'cash' THEN p.order_id END) AS cash_orders,
        COUNT(DISTINCT CASE WHEN p.method = 'momo' THEN p.order_id END) AS momo_orders,
        COUNT(DISTINCT CASE WHEN p.method = 'bank' THEN p.order_id END) AS bank_orders
    FROM Orders o
    LEFT JOIN Payment p ON o.order_id = p.order_id
    WHERE CAST(o.order_time AS DATE) = @date
      AND o.status = 'paid';
END;
GO

CREATE FUNCTION fn_GetRevenueByDate(@date DATE)
RETURNS DECIMAL(12,2)
AS
BEGIN
    DECLARE @total DECIMAL(12,2);

    SELECT @total = SUM(total)
    FROM Orders
    WHERE CAST(order_time AS DATE) = @date
      AND status = 'paid';

    RETURN ISNULL(@total, 0);
END;
GO

CREATE PROCEDURE sp_RegisterStaff
    @username VARCHAR(50),
    @password VARCHAR(255),
    @full_name NVARCHAR(100),
    @email VARCHAR(100),
    @role VARCHAR(20)
AS
BEGIN
    SET NOCOUNT ON;

    IF @role NOT IN ('admin','staff')
    BEGIN
        RAISERROR(N'Vai trò không hợp lệ', 16, 1);
        RETURN;
    END;

    IF EXISTS (SELECT 1 FROM [User] WHERE username = @username)
    BEGIN
        RAISERROR(N'Tên đăng nhập đã tồn tại', 16, 1);
        RETURN;
    END;

    INSERT INTO [User] (username, password, role, full_name, email, is_active)
    VALUES (@username, @password, @role, @full_name, @email, 1);

    SELECT CONVERT(INT, SCOPE_IDENTITY()) AS user_id;
END;
GO

CREATE PROCEDURE sp_RegisterCustomer
    @phone VARCHAR(15),
    @password VARCHAR(255),
    @full_name NVARCHAR(100),
    @email VARCHAR(100),
    @address NVARCHAR(200)
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS (SELECT 1 FROM Customer WHERE phone = @phone)
    BEGIN
        RAISERROR(N'Số điện thoại đã được đăng ký', 16, 1);
        RETURN;
    END;

    INSERT INTO Customer (phone, password, full_name, email, address)
    VALUES (@phone, @password, @full_name, @email, @address);

    SELECT CONVERT(INT, SCOPE_IDENTITY()) AS customer_id;
END;
GO

CREATE PROCEDURE sp_LoginStaff
    @username VARCHAR(50),
    @password VARCHAR(255)
AS
BEGIN
    SET NOCOUNT ON;

    SELECT user_id, username, role, full_name, is_active
    FROM [User]
    WHERE username = @username
      AND password = @password
      AND is_active = 1;
END;
GO

-- Customer password authentication is performed in Java with BCrypt.
-- This legacy procedure does not verify BCrypt hashes and is not used by the application.
CREATE PROCEDURE sp_LoginCustomer
    @phone VARCHAR(15),
    @password VARCHAR(255)
AS
BEGIN
    SET NOCOUNT ON;

    SELECT customer_id, phone, full_name, email, address, total_spent, is_active
    FROM Customer
    WHERE phone = @phone
      AND password = @password
      AND is_active = 1;
END;
GO

